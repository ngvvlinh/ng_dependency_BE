CREATE TABLE if not exists shop (
    id bigint PRIMARY KEY,
    rid bigint,
    name text,
    owner_id bigint,
    status int2,
    created_at timestamp with time zone DEFAULT date_trunc('second', now()),
    updated_at timestamp with time zone,
    rules jsonb,
    image_url text,
    phone text,
    website_url text,
    email text,
    address_id bigint,
    bank_account jsonb,
    contact_persons jsonb,
    code text,
    auto_create_ffm boolean DEFAULT FALSE,
    recognized_hosts text[],
    ship_to_address_id int8,
    ship_from_address_id int8,
    ghn_note_code ghn_note_code,
    try_on try_on,
    company_info jsonb,
    survey_info jsonb,
    money_transaction_rrule text
);